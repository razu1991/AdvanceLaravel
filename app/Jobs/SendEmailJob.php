    /**
     * Create a new job instance.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Execute the job.
     *
     * @return void
     */
    public function handle()
    {
       Mail::to('shahnaouzrazu21@gmail.com')->send(new SendEmailMailable()); 
    }
}
